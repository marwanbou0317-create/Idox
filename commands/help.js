function handle(event, api, role) {
  const { threadID } = event;

  let msg = `📋 الأوامر المتاحة:

🔹 للجميع:
/ping — التحقق من تشغيل البوت
/سيرفر — معلومات السيرفر
/ابتيم — وقت التشغيل
/اوامر — هذه القائمة
`;

  if (role === 'admin' || role === 'superadmin') {
    msg += `
🔧 أوامر المشرف:
/قائمة — القائمة التفاعلية الرقمية (14 خياراً) 🆕
/محرك — تشغيل/إيقاف المحرك العادي
/محرك رسالة [نص]
/محرك وقت [ثواني] (≥10)
/محرك الذكي — تشغيل/إيقاف المحرك الذكي
/محرك حالة

/قفل — قفل/فتح المجموعة
/قفل الذكي — قفل كل المجموعات
/قفل حالة

/كنية @شخص [كنية] — تعيين كنية
/كنية @شخص reset — مسح الكنية
/كنية الكل [كنية|reset]
/كنيات [نص|reset] — نفس الكنية للجميع

/تثبيت @شخص [كنية] — تثبيت كنية وحمايتها
/تثبيت @شخص إلغاء — رفع حماية الكنية
/تثبيت قائمة — عرض الكنيات المحمية

/اسم [الاسم] — تثبيت اسم المجموعة
/اسم إلغاء — إلغاء حماية الاسم
/اسم عرض — عرض حالة الحماية

/تعيين تلقائي عام — تعيين اسم تلقائياً كل ساعة
/تعيين تلقائي يومي — تعيين اسم يومياً
/تعيين تلقائي ساعة [0-23] — تعيين في ساعة محددة
/تعيين تلقائي توقيت [دقائق] — تعيين بفترة دقائق
/تعيين تلقائي حالة — عرض الأنماط المتاحة

/رسائل إضافة [وقت] [نص]
/رسائل قائمة | حالة | إيقاف | تشغيل | حذف

/تعذيب @شخص أو (رد على رسالة) — طرد وإعادة ×10
`;
  }

  if (role === 'superadmin') {
    msg += `
👑 أوامر سوبر أدمن:
/رفع @شخص — رفع لمشرف
/اخفاض @شخص — إنزال من مشرف
/ابادة تأكيد — طرد كل الأعضاء عدا الأدمن ⚠️
/حماية ايقاف — وقف احترازي 3د
/حماية رفع — رفع الإيقاف
/حماية — حالة نظام مكافحة الحظر
`;
  }

  const roleText =
    role === 'superadmin'
      ? '👑 سوبر أدمن'
      : role === 'admin'
      ? '🔧 مشرف'
      : '👤 عضو';

  msg += `

🪩 رتبتك: ${roleText}`;

  api.sendMessage(msg, threadID);
}

module.exports = { handle };
