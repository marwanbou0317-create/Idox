function handle(event, api, role) {
  const { threadID } = event;

  let msg = '📋 الأوامر المتاحة:\n\n'
    + '🔹 للجميع:\n'
    + '/ping — التحقق من تشغيل البوت\n'
    + '/سيرفر — معلومات السيرفر\n'
    + '/ابتيم — وقت التشغيل\n'
    + '/اوامر — هذه القائمة\n';

  if (role === 'admin' || role === 'superadmin') {
    msg += '\n🔧 أوامر المشرف:\n'
      + '/محرك — تشغيل/إيقاف المحرك\n'
      + '/محرك رسالة [نص]\n'
      + '/محرك وقت [ثواني] (≥10)\n'
      + '/محرك الذكي — وضع ذكي\n'
      + '/محرك حالة\n\n'
      + '/قفل — قفل/فتح المجموعة\n'
      + '/قفل الذكي — قفل كل المجموعات\n'
      + '/قفل حالة\n\n'
      + '/كنية @شخص [كنية] — تعيين كنية\n'
      + '/كنية @شخص reset — مسح الكنية\n'
      + '/كنية الكل [كنية|reset]\n'
      + '/كنيات [نص|reset] — نفس الكنية للجميع\n\n'
      + '/رسائل إضافة [وقت] [نص]\n'
      + '/رسائل قائمة | حالة | إيقاف | تشغيل | حذف\n';
  }

  if (role === 'superadmin') {
    msg += '\n👑 أوامر سوبر أدمن:\n'
      + '/رفع @شخص — رفع لمشرف\n'
      + '/اخفاض @شخص — إنزال من مشرف\n';
  }

  const roleText = role === 'superadmin' ? '👑 سوبر أدمن' : role === 'admin' ? '🔧 مشرف' : '👤 عضو';
  msg += '\n🪪 رتبتك: ' + roleText;

  api.sendMessage(msg, threadID);
}

module.exports = { handle };
